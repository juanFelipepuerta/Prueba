package co.com.traductor.datadriven.model;

public class GoogleTraslateData {
    private String origen;
    private String destino;
    private String palabrai;
    private String palabrae;

    public String getOrigen() {
        return origen;
    }

    public void setOrigen(String origen) {
        this.origen = origen;
    }

    public String getDestino() {
        return destino;
    }

    public void setDestino(String destino) {
        this.destino = destino;
    }

    public String getPalabrai() {
        return palabrai;
    }

    public void setPalabrai(String palabrai) {
        this.palabrai = palabrai;
    }

    public String getPalabrae() {
        return palabrae;
    }

    public void setPalabrae(String palabrae) {
        this.palabrae = palabrae;
    }
}
